`a`.`shouhin_mr_cd` AS `shouhin_mr_cd`,
`b`.`tantou_mr_cd` AS `tantou_mr_cd`,
`a`.`tanni_mr1_cd` AS `tanni_mr1_cd`,
`a`.`tanni_mr2_cd` AS `tanni_mr2_cd`,
`a`.`iro` AS `iro`,
`a`.`iromei` AS `iromei`,
`a`.`lot` AS `lot`,
`a`.`kobetucd` AS `kobetucd`,
`a`.`hinsitu_kbn_cd` AS `hinsitu_kbn_cd`,
`d`.`hinsitu_hyouka_kbn_cd` AS `hinsitu_hyouka_kbn_cd`,
`a`.`souko_mr_cd` AS `souko_mr_cd`,
NULL AS `nyuukobis`,
`b`.`uriagebi` AS `shukkobis`,
`b`.`uriagebi` AS `nyuushukkobi`,
date_format(`b`.`uriagebi`,'%Y%m') AS `nyuushukkoym`,
'uriage' AS `denpyou_mr_cd`,
`b`.`id` AS `id`,
`b`.`cd` AS `cd`,
`a`.`id` AS `meisai_id`,
`a`.`cd` AS `meisai_cd`,
`a`.`utiwake_kbn_cd` AS `utiwake_kbn_cd`,
`b`.`tokuisaki_mr_cd` AS `torihikisaki_cd`,
`a`.`bikou` AS `bikou`,
(0 - `a`.`suuryou1`*`c`.`uriage_zaiko_flg`) AS `zaiko_ryou1s`,
(0 - `a`.`suuryou2`*`c`.`uriage_zaiko_flg`) AS `zaiko_ryou2s`,
if(`a`.`tanka_kbn`=1,`a`.`tanni_mr1_cd`,`a`.`tanni_mr2_cd`) AS tanka_tanni_mr_cd,
'0000-00-000' AS `shiirebi_tankas`,
