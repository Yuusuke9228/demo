(case `c`.`uriage_azukari_flg` when -(1) then `a`.`suuryou1` else 0 end) AS `azukari_hiki1s`,
(case `c`.`uriage_azukari_flg` when -(1) then `a`.`suuryou2` else 0 end) AS `azukari_hiki2s`
