join `utiwake_kbns` `c` on((`c`.`cd` = `a`.`utiwake_kbn_cd`))) 
join `hinsitu_kbns` `d` on((`d`.`cd` = `a`.`hinsitu_kbn_cd`))) 
where ((`c`.`uriage_zaiko_flg` <> 0) or (`c`.`juchuu_zan_flg` <> 0) or (`c`.`uriage_azukari_flg` <> 0))
